// 获取应用使用映射数据（支持自定义映射扩展）
export const getAppUsageMapping = (customMapping = []) => {
  // 合并默认映射和自定义映射，避免重复
  const allMappings = [...defaultAppUsageMapping];
  const existingAppNames = new Set(allMappings.map((item) => item.app_name));

  customMapping.forEach((item) => {
    if (!existingAppNames.has(item.app_name)) {
      allMappings.push(item);
      existingAppNames.add(item.app_name);
    }
  });

  return allMappings;
};

// 根据应用名称获取应用使用描述
export const getAppUsageDescription = (appName, customMapping = []) => {
  // 添加调试信息
  // console.log("检查应用描述映射:", {
  //   appName,
  //   hasAppName: !!appName,
  //   mappingCount: getAppUsageMapping(customMapping).length,
  //   sampleMappings: getAppUsageMapping(customMapping)
  //     .slice(0, 3)
  //     .map((m) => m.app_name),
  // });

  if (!appName || appName === "未知应用") {
    return "锁屏状态";
  }

  const mapping = getAppUsageMapping(customMapping).find(
    (item) => item.app_name === appName
  );
  if (mapping) {
    // console.log("找到匹配的应用描述:", { appName, usage: mapping.usage });
    return mapping.usage;
  }

  // 没有匹配的应用名称时返回默认描述
  // console.log("未找到匹配的应用描述:", { appName });
  return `${appName}，如名称 你应该知道在做什么...`;
};
